package java1;

public class Data_Types {

    public static void main(String[] args) {
    
        
//  Booleans
        
        boolean b = true;
        
        System.out.println("Boolean b: " + b);    // Prints the value of the boolean variable 'b'.
        
//      char
    
    char c = 'h';
    
    System.out.println("Char c: " + c);    // Prints the value of the char variable 'c'.
    
    
//  byte
    
    byte bi = 67;
    System.out.println("Byte bi: " + bi);    // Prints the value of the byte variable 'bi'.
    
    
//  string
    
    String s1 = "new delhi"; // Changed to 's1' to avoid conflict with 'Sl'
    System.out.println("String s1: " + s1);    // Prints the value of the String variable 's1'.
    
    
//  short
    short s2 = -25676;
    System.out.println("Short s2: " + s2);    // Prints the value of the short variable 's2'.
    
    
//  int 
    
    int i = 19841864;
    System.out.println("Int i: " + i);    // Prints the value of the int variable 'i'.
    
    
//   long 
    
    long l = 8555956595944996L; // Note the 'L' for long literal.
    System.out.println("Long l: " + l);    // Prints the value of the long variable 'l'.
    
    
//  float
    
    float f1 = 58998595.5680f; // Note the 'f' for float literal.
    System.out.println("Float f1: " + f1);    // Prints the value of the float variable 'f1'.
    
    
    
//  double
    
    double d1 = 58949496595.55659594984d; // Note the 'd' for double literal.
    System.out.println("Double d1: " + d1);    // Prints the value of the double variable 'd1'.
    
    
    }

}